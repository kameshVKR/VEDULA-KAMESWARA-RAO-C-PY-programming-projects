operator = input("enter your operator(+, -, *, /): ")
first_value = int(input("enter your first value: "))
second_value = int(input("enter your second value: "))
if operator == '+':
 print(first_value + second_value)
elif operator == '-':
  print(first_value - second_value)
elif operator == '*':
  print(first_value * second_value)
elif operator == '/':
  print(first_value / second_value)
else : print("error999+: invalid input")

